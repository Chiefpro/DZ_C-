static int[,] NewArray()
{
    int[,] arr = new int[2, 6];
    for (int i = 0; i < arr.GetLength(0); i++)
    {
        for (int j = 0; j < arr.GetLength(1); j++)
            arr[i, j] = new Random().Next(1, 10);
    }
    return arr;
}
static void PrintArray(int[,] arr)
{
    for (int i = 0; i < arr.GetLength(0); i++)
    {
        for (int j = 0; j < arr.GetLength(1); j++)
            Console.Write($"{arr[i, j]} ");
        Console.WriteLine();

    }
}
static void PrintPic(int[,] arr)
{
    for (int i = 0; i < arr.GetLength(0); i++)
    {
        for (int j = 0; j < arr.GetLength(1); j++)
        {
            if (arr[i, j] == 0) Console.Write(" ");
            else Console.Write("+");
        }
        Console.WriteLine();
    }
}
static int[,] Sort(int[,] arr, int a, int b)
{
    if (arr[a, b] == 0)
    {
        arr[a, b] = 1;
        Sort(arr, a - 1, b);
        Sort(arr, a, b - 1);
        Sort(arr, a + 1, b);
        Sort(arr, a, b + 1);
    }
    return arr;
}
static int Fact(int f)
{
    int sum = 1;
    if (f == 1) return f;
    else sum = f * Fact(f-1);
    return sum;
}

